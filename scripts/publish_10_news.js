async function run() {
    console.log(`Starting to publish ${articles.length} articles...`);
    for (const data of articles) {
        try {
            const catId = await getCategory(data.categorySlug, data.categoryTitle);
            const imageAssetId = await uploadImage(data.imagePrefix);

            const bodyBlocks = data.bodyText.split('।\n\n').map(p => ({
                _type: 'block',
                style: 'normal',
                children: [{ _type: 'span', marks: [], text: p + '।' }]
            }));

            const doc = {
                _type: 'article',
                title: data.title,
                slug: { current: data.slug + '-' + Math.random().toString(36).substring(7), _type: 'slug' },
                excerpt: data.excerpt,
                body: bodyBlocks,
                category: { _type: 'reference', _ref: catId },
                district: data.district,
                publishedAt: new Date().toISOString(),
            };

            if (imageAssetId) {
                doc.featureImage = {
                    _type: 'image',
                    asset: { _type: 'reference', _ref: imageAssetId },
                    alt: data.title
                };
            }

            console.log('Creating article:', data.title);
            const result = await client.create(doc);
            console.log('Created success:', result._id);
        } catch (err) {
            console.error('Error creating article:', data.title, err.message);
        }
    }
}

run().catch(console.error);
