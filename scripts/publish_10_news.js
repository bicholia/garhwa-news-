// Since images are saved in C:\Users\LAPPY PLUS\.gemini\antigravity\brain\b62b5f09-4d49-49a7-9d64-9c329f386dcb\
const imageDir = 'C:\\Users\\LAPPY PLUS\\.gemini\\antigravity\\brain\\b62b5f09-4d49-49a7-9d64-9c329f386dcb';

function getLatestImage(prefix) {
    const files = fs.readdirSync(imageDir);
    const matching = files.filter(f => f.startsWith(prefix) && f.endsWith('.png'));
    if (matching.length === 0) return null;
    matching.sort((a, b) => {
        return fs.statSync(path.join(imageDir, b)).mtimeMs - fs.statSync(path.join(imageDir, a)).mtimeMs;
    });
    return path.join(imageDir, matching[0]);
}

async function uploadImage(imagePrefix) {
    const filePath = getLatestImage(imagePrefix);
    if (!filePath) {
        console.warn('Image not found for prefix:', imagePrefix);
        return null;
    }
    console.log(`Uploading image ${imagePrefix}...`);
    const buffer = fs.readFileSync(filePath);
    const asset = await client.assets.upload('image', buffer, {
        filename: path.basename(filePath),
    });
    console.log(`Uploaded image: ${imagePrefix} -> ${asset._id}`);
    return asset._id;
}

// Ensure category exists
async function getCategory(slug, title) {
    const existing = await client.fetch(`*[_type == "category" && slug.current == $slug][0]`, { slug });
    if (existing) return existing._id;
    const doc = {
        _type: 'category',
        title,
        slug: { current: slug, _type: "slug" }
    };
    const res = await client.create(doc);
    return res._id;
}

