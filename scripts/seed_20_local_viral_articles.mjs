async function seedLocalViralArticles() {
    console.log('🚀 Starting Seeding of 20 Local Viral Articles (Garhwa & Palamu)...');

    try {
        const authorId = 'author-admin';

        // Fetch categories
        const categories = await client.fetch(`*[_type == "category"]{_id, "slug": slug.current}`);
        const categoryMap = {};
        categories.forEach(cat => {
            categoryMap[cat.slug] = cat._id;
        });

        // Default categories mapping based on user headings
        const catMapping = {
            'प्रशासन': 'administration',
            'स्वास्थ्य': 'health',
            'ग्रामीण विकास': 'rural-development',
            'क्राइम': 'crime'
        };

        const imageAsset = await client.fetch(`* [_type == "sanity.imageAsset"][0]{ _id }`);

        const articlesRaw = fullArticlesContent.split(/# आर्टिकल \d+:/).filter(raw => {
            const trimmed = raw.trim();
            return trimmed && !trimmed.startsWith('# 🏛️') && !trimmed.startsWith('# 🏥') && !trimmed.startsWith('# 🌾') && !trimmed.startsWith('# 🚔');
        });

        for (const raw of articlesRaw) {
            const lines = raw.trim().split('\n');
            const mainTitleLine = lines.find(l => l.trim().startsWith('###') === false && l.trim().length > 0) || '';
            const subTitleLine = lines.find(l => l.startsWith('### ')) || '';
            const datelineLine = lines.find(l => l.includes('📅 डेटलाइन:')) || '';

            const mainTitle = mainTitleLine.replace('# ', '').trim();
            const subTitle = subTitleLine.replace('### ', '').trim();

            if (!mainTitle || mainTitle.length < 5) continue;

            const districtRaw = datelineLine.includes('गढ़वा') ? 'garhwa' : (datelineLine.includes('पलामू') ? 'palamu' : 'jharkhand');

            // Find category from keywords
            let categorySlug = 'general';
            const lowerRaw = raw.toLowerCase();
            if (lowerRaw.includes('प्रशासन') || lowerRaw.includes('bdo') || lowerRaw.includes('dm') || lowerRaw.includes('तबादले')) categorySlug = 'administration';
            else if (lowerRaw.includes('स्वास्थ्य') || lowerRaw.includes('मलेरिया') || lowerRaw.includes('डेंगू') || lowerRaw.includes('अस्पताल') || lowerRaw.includes('कुपोषण') || lowerRaw.includes('टीकाकरण')) categorySlug = 'health';
            else if (lowerRaw.includes('ग्रामीण विकास') || lowerRaw.includes('आवास योजना') || lowerRaw.includes('सड़क निर्माण') || lowerRaw.includes('बिजली कनेक्शन') || lowerRaw.includes('नल-जल') || lowerRaw.includes('स्वच्छ भारत')) categorySlug = 'rural-development';
            else if (lowerRaw.includes('क्राइम') || lowerRaw.includes('लूट') || lowerRaw.includes('दुष्कर्म') || lowerRaw.includes('हथियार') || lowerRaw.includes('चोरी') || lowerRaw.includes('मुठभेड़')) categorySlug = 'crime';

            const body = [];
            let inList = false;
            let inTable = false;

            lines.forEach((line, index) => {
                if (index === 0 || line.startsWith('### ') || line.includes('📅 डेटलाइन:')) return;
                if (line.trim() === '---' || line.trim() === '') return;

                if (line.startsWith('- ') || line.startsWith('* ')) {
                    body.push({
                        _type: 'block',
                        style: 'normal',
                        listItem: 'bullet',
                        children: [{ _type: 'span', text: line.replace(/^[*-] /, '').trim(), marks: [] }]
                    });
                } else if (/^\\d+\\./.test(line)) {
                    body.push({
                        _type: 'block',
                        style: 'normal',
                        listItem: 'number',
                        children: [{ _type: 'span', text: line.replace(/^\\d+\\. /, '').trim(), marks: [] }]
                    });
                } else if (line.startsWith('> ')) {
                    body.push({
                        _type: 'block',
                        style: 'blockquote',
                        children: [{ _type: 'span', text: line.replace('> ', '').replace(/"/g, '').trim(), marks: [] }]
                    });
                } else if (line.startsWith('### ')) {
                    body.push({
                        _type: 'block',
                        style: 'h3',
                        children: [{ _type: 'span', text: line.replace('### ', '').trim(), marks: [] }]
                    });
                } else if (line.startsWith('## ')) {
                    body.push({
                        _type: 'block',
                        style: 'h2',
                        children: [{ _type: 'span', text: line.replace('## ', '').trim(), marks: [] }]
                    });
                } else if (line.includes('|')) {
                    // Simple table handling - convert to bold key-value pairs or just text
                    if (!line.includes('---')) {
                        body.push({
                            _type: 'block',
                            style: 'normal',
                            children: [{ _type: 'span', text: line.split('|').filter(Boolean).map(s => s.trim()).join(' | '), marks: ['strong'] }]
                        });
                    }
                } else {
                    body.push({
                        _type: 'block',
                        style: 'normal',
                        children: [{ _type: 'span', text: line.trim(), marks: [] }]
                    });
                }
            });

            const slugBase = mainTitle.slice(0, 50)
                .replace(/\s+/g, '-')
                .replace(/[^\u0900-\u097fa-zA-Z0-9\-]/g, '')
                .toLowerCase();
            const slug = `${slugBase}-${Math.floor(Math.random() * 10000)}`;

            const doc = {
                _type: 'article',
                title: mainTitle,
                slug: { _type: 'slug', current: slug },
                excerpt: subTitle,
                body: body,
                category: { _type: 'reference', _ref: categoryMap[categorySlug] || categoryMap['top-story'] },
                author: { _type: 'reference', _ref: authorId },
                district: districtRaw,
                featured: true,
                publishedAt: new Date().toISOString(),
                featureImage: imageAsset ? {
                    _type: 'image',
                    asset: { _type: "reference", _ref: imageAsset._id },
                    alt: mainTitle
                } : undefined
            };

            await client.create(doc);
            console.log(`✅ Article Seeded: ${mainTitle} (${categorySlug} - ${districtRaw})`);
        }

        console.log('✨ Mission Completed! 20 Garhwa & Palamu Viral Articles are now live!');
    } catch (err) {
        console.error('❌ Seeding Error:', err.message);
    }
}

seedLocalViralArticles();
