async function downloadAndUploadImage(article) {
    // Pollinations AI is restricted (401), switching to Picsum Photos for reliable high-quality images
    const seed = encodeURIComponent(article.title.slice(0, 50));
    const imageUrl = `https://picsum.photos/seed/${seed}/1024/768`;

    const filename = `${article.district}-${Date.now()}.jpg`;
    const tempPath = join(__dirname, filename);

    try {
        console.log(`Downloading image for: ${article.title}...`);
        const response = await fetch(imageUrl);
        if (!response.ok) throw new Error(`Fetch failed: ${response.statusText}`);
        
        const arrayBuffer = await response.arrayBuffer();
        const buffer = Buffer.from(arrayBuffer);
        fs.writeFileSync(tempPath, buffer);

        console.log(`Uploading image to Sanity...`);
        const asset = await client.assets.upload('image', buffer, {
            filename: filename,
            contentType: 'image/jpeg'
        });
        
        fs.unlinkSync(tempPath); // Delete temp file
        return asset._id;
    } catch (err) {
        if (fs.existsSync(tempPath)) fs.unlinkSync(tempPath);
        throw err;
    }
}

async function run() {
    console.log('🌟 Starting Bulk News Addition (20 Articles)...');

    for (let i = 0; i < articles.length; i++) {
        const article = articles[i];
        console.log(`\n[${i + 1}/20] Processing: ${article.title}`);

        try {
            // 1. Image
            const assetId = await downloadAndUploadImage(article);

            // 2. Slug
            const baseSlug = article.title.toLowerCase()
                .replace(/\s+/g, '-')
                .replace(/[^\u0900-\u097fa-zA-Z0-9\-]/g, '')
                .slice(0, 80);
            const slug = `${baseSlug}-${Date.now()}-${i}`;

            // 3. Document
            const doc = {
                _type: 'article',
                title: article.title,
                slug: { _type: 'slug', current: slug },
                excerpt: article.excerpt,
                body: [
                    {
                        _type: 'block',
                        style: 'normal',
                        children: [{ _type: 'span', text: article.body }]
                    }
                ],
                category: { _type: 'reference', _ref: article.category },
                district: article.district,
                publishedAt: new Date(Date.now() - (i * 1800000)).toISOString(), // 30 min intervals
                featureImage: {
                    _type: 'image',
                    asset: { _type: 'reference', _ref: assetId }
                }
            };

            const result = await client.create(doc);
            console.log(`✅ Created! ID: ${result._id}`);
        } catch (err) {
            console.error(`❌ Error creating article "${article.title}":`);
            console.error(err);
        }
    }

    console.log('\n✨ All 20 news articles published successfully!');
}

run();
