async function downloadAndUploadImage(article, i) {
    // Using themed seeding for variety but relevance
    const categoryKeywords = {
        "category-crime": ["police", "patrol", "handcuffs"],
        "category-public-issues": ["protest", "crowd", "meeting"],
        "category-rural-development": ["village", "farm", "forest"],
        "category-health-education": ["exam", "student", "classroom"],
        "category-religion": ["temple", "prayer", "ritual"],
        "category-politics": ["parliament", "speech", "leader"],
        "category-social-events": ["festival", "celebration", "holi"],
        "category-top-story": ["news", "breaking", "accident"]
    };
    
    const keywords = categoryKeywords[article.category] || ["news"];
    const seed = encodeURIComponent(keywords[i % keywords.length] + "-" + i);
    const imageUrl = `https://picsum.photos/seed/${seed}/1200/800`;

    const filename = `${article.district}-today-${i}-${Date.now()}.jpg`;
    const tempPath = join(__dirname, filename);

    try {
        console.log(`[${i+1}] Downloading image for: ${article.title}...`);
        const response = await fetch(imageUrl);
        if (!response.ok) throw new Error(`Fetch failed: ${response.statusText}`);
        
        const arrayBuffer = await response.arrayBuffer();
        const buffer = Buffer.from(arrayBuffer);
        fs.writeFileSync(tempPath, buffer);

        const asset = await client.assets.upload('image', buffer, {
            filename: filename,
            contentType: 'image/jpeg'
        });
        
        fs.unlinkSync(tempPath);
        return asset._id;
    } catch (err) {
        if (fs.existsSync(tempPath)) fs.unlinkSync(tempPath);
        throw err;
    }
}

async function run() {
    console.log('🚀 Starting Publication of 15 Garhwa News Articles...');
    const batchId = `bulk-garhwa-${Date.now()}`;

    for (let i = 0; i < articles.length; i++) {
        const article = articles[i];
        console.log(`\n[${i + 1}/15] Processing: ${article.title}`);

        try {
            const assetId = await downloadAndUploadImage(article, i);

            const baseSlug = article.title.toLowerCase()
                .replace(/\s+/g, '-')
                .replace(/[^\u0900-\u097fa-zA-Z0-9\-]/g, '')
                .slice(0, 80);
            const slug = `${baseSlug}-${Date.now()}-${i}`;

            const doc = {
                _type: 'article',
                title: article.title,
                slug: { _type: 'slug', current: slug },
                excerpt: article.excerpt,
                body: [
                    {
                        _type: 'block',
                        style: 'normal',
                        children: [{ _type: 'span', text: article.body }]
                    }
                ],
                category: { _type: 'reference', _ref: article.category },
                district: article.district,
                publishedAt: new Date(Date.now() - (i * 3600000)).toISOString(), // 1 hour intervals back in time
                featureImage: {
                    _type: 'image',
                    asset: { _type: 'reference', _ref: assetId }
                },
                importMetadata: {
                    batchId: batchId,
                    importedAt: new Date().toISOString(),
                    source: 'bulk-upload',
                    autoCategory: true
                }
            };

            const result = await client.create(doc);
            console.log(`✅ Success! Sanity ID: ${result._id}`);
        } catch (err) {
            console.error(`❌ Fatal Error for "${article.title}":`, err.message);
        }
    }

    console.log('\n✅ All 15 articles have been published to Sanity CMS!');
}

run();
